// Regenerate src/generated/node-types.ts: the grammar-derived unions that type `find`, `field`,
// and `RichNode.type`. Reads each grammar's `node-types.json` and emits sorted, deduped
// string-literal unions — deterministic, so re-running is a no-op unless a grammar changed.
//
//   pnpm --filter @codegraft/core regen-node-types
//
// typescript/tsx are read from the vendored copies under vendor/, written by regen-ts-wasm.sh from
// the same grammar tarball as the wasm — so the types always describe the grammar the wasm parses.
// javascript/html/css are read from their installed npm packages (core devDependencies).
import { createRequire } from 'node:module'
import { readFileSync, writeFileSync } from 'node:fs'

const require = createRequire(import.meta.url)
const read = (url) => JSON.parse(readFileSync(new URL(url, import.meta.url), 'utf8'))

// The vendored typescript/tsx grammars carry no version of their own; they track the wasm ref.
const tsRef = readFileSync(new URL('./regen-ts-wasm.sh', import.meta.url), 'utf8').match(/ref="\$\{1:-v?([0-9.]+)\}"/)[1]

// grammar id → { nodeTypes, version }. Order here is the order types are emitted in.
const SOURCES = {
  javascript: fromPackage('tree-sitter-javascript', 'src/node-types.json'),
  typescript: { nodeTypes: read('../vendor/tree-sitter-typescript.node-types.json'), version: `tree-sitter-typescript@${tsRef}` },
  tsx: { nodeTypes: read('../vendor/tree-sitter-tsx.node-types.json'), version: `tree-sitter-typescript@${tsRef}` },
  html: fromPackage('tree-sitter-html', 'src/node-types.json'),
  css: fromPackage('tree-sitter-css', 'src/node-types.json'),
  // Vendored alongside the wasm from `@tree-sitter-grammars/tree-sitter-yaml` (no bare-package wasm).
  yaml: { nodeTypes: read('../vendor/tree-sitter-yaml.node-types.json'), version: '@tree-sitter-grammars/tree-sitter-yaml@0.7.1' },
}

function fromPackage(pkg, sub) {
  return {
    nodeTypes: JSON.parse(readFileSync(require.resolve(`${pkg}/${sub}`), 'utf8')),
    version: `${pkg}@${require(`${pkg}/package.json`).version}`,
  }
}

const grammars = Object.entries(SOURCES).map(([id, { nodeTypes, version }]) => ({
  id,
  version,
  pascal: id[0].toUpperCase() + id.slice(1),
  named: literals(nodeTypes.filter((n) => n.named).map((n) => n.type)),
  all: literals(nodeTypes.map((n) => n.type)),
  fields: literals(nodeTypes.flatMap((n) => Object.keys(n.fields ?? {}))),
}))

writeFileSync(new URL('../src/generated/node-types.ts', import.meta.url), render(grammars))
console.log(`wrote src/generated/node-types.ts (${grammars.map((g) => `${g.id}:${g.named.length}`).join(' ')})`)

/** Sorted, deduped, TS-quoted string literals for a union body. */
function literals(values) {
  return [...new Set(values)].sort((a, b) => a.localeCompare(b)).map((v) => JSON.stringify(v))
}

/** A `type Name = a | b | …` (or `= never` when empty), wrapped one-per-line. */
function union(name, members) {
  if (members.length === 0) return `export type ${name} = never`
  return `export type ${name} =\n  | ${members.join('\n  | ')}`
}

function render(grammars) {
  const versions = [...new Set(grammars.map((g) => g.version))]
  const grammarIds = grammars.map((g) => `'${g.id}'`).join(' | ')
  const combined = (suffix) => grammars.map((g) => `${g.pascal}${suffix}`)

  const blocks = [
    '// Generated by `pnpm --filter @codegraft/core regen-node-types` — do not edit by hand.',
    `// source: ${versions.join(', ')}`,
    '// A dependency-free leaf module: imports nothing, so it never cycles with types.ts.',
    '',
    '// Named node types per grammar — including supertype names (`statement`, `expression`, …),',
    '// which are the structural types `find`/`closest`/`ancestors`/`isOfType` can match.',
    ...grammars.map((g) => union(`${g.pascal}NodeType`, g.named)),
    '',
    '// Every node type per grammar, adding the anonymous keyword/punctuation tokens (`type`, `,`,',
    "// `=>`, …) that appear as a node's `type` but are never `find` targets.",
    ...grammars.map((g) => union(`${g.pascal}NodeTypeAll`, g.all)),
    '',
    '// Field names per grammar (the keys of `field()`/`setField()` and object-form `find` matchers).',
    ...grammars.map((g) => union(`${g.pascal}FieldName`, g.fields)),
    '',
    '/** Every named node type across the built-in grammars — the `find` domain. */',
    union('NodeType', combined('NodeType')),
    '',
    '/** Every node type, named or anonymous — the `RichNode.type` domain. */',
    union('NodeTypeAll', combined('NodeTypeAll')),
    '',
    '/** Every field name across the built-in grammars. */',
    union('FieldName', combined('FieldName')),
    '',
    '// Per-grammar projections — `…Of<G>` selects one grammar (and distributes over a union of',
    "// grammars). `…Of<GrammarId>` equals the combined union, so they're drop-in for the defaults.",
    ...projection('NodeTypeOf', 'NodeType', 'a node type'),
    ...projection('NodeTypeAllOf', 'NodeTypeAll', 'a node type (named or anonymous)'),
    ...projection('FieldNameOf', 'FieldName', 'a field name'),
  ]
  return blocks.join('\n') + '\n'

  function projection(name, suffix, what) {
    return [
      `/** The ${what} valid in grammar \`G\` — e.g. \`${name}<'typescript'>\`. */`,
      `export type ${name}<G extends ${grammarIds}> =`,
      ...grammars.map((g) => `  G extends '${g.id}' ? ${g.pascal}${suffix} :`),
      '  never',
      '',
    ]
  }
}
